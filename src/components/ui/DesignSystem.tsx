// Status Indicators
export const StatusIndicator = ({
  status,
  size = 'md'
}: {
  status: 'online' | 'offline' | 'pending' | 'verified' | 'error';
  size?: 'sm' | 'md' | 'lg';
}) => {
  const sizeClasses = {
    sm: 'w-2 h-2',
    md: 'w-3 h-3',
    lg: 'w-4 h-4'
  };

  const statusConfig = {
    online: { color: 'bg-trust-500', animation: 'animate-pulse' },
    offline: { color: 'bg-neutral-500', animation: '' },
    pending: { color: 'bg-warning-500', animation: 'animate-pulse' },
    verified: { color: 'bg-trust-500', animation: '' },
    error: { color: 'bg-error-500', animation: 'animate-pulse' },
  };

  const config = statusConfig[status];

  return (
    <div className={`${sizeClasses[size]} ${config.color} ${config.animation} rounded-full`} />
  );
};

// Security Level Badge
export const SecurityLevelBadge = ({
  level
}: {
  level: 'standard' | 'enhanced' | 'maximum'
}) => {
  const levelConfig = {
    standard: {
      color: 'bg-warning-500/20 text-warning-300 border-warning-500/30',
      icon: SecurityIcons.Lock,
      label: 'Standard'
    },
    enhanced: {
      color: 'bg-primary-500/20 text-primary-300 border-primary-500/30',
      icon: SecurityIcons.Shield,
      label: 'Enhanced'
    },
    maximum: {
      color: 'bg-trust-500/20 text-trust-300 border-trust-500/30',
      icon: SecurityIcons.Verified,
      label: 'Maximum'
    }
  };

  const config = levelConfig[level];
  const Icon = config.icon;

  return (
    <div className={`inline-flex items-center space-x-2 px-3 py-1 rounded-full border text-sm font-medium ${config.color}`}>
      <Icon className="w-4 h-4" />
      <span>{config.label}</span>
    </div>
  );
};

